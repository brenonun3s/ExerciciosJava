
public class LacoForEach {

    public static void main(String[] args) {

        String vect[] = new String[] { "Maria", "João", "Luiza" };

        for (String obj : vect) { // Aqui, você adiciona o tipo de dado, um nome para o laço e onde deseja buscar
            System.out.println(obj);

        }

    }
}
